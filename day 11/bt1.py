# Add function
def add_number(x,y):
    add_func = x + y
    return add_func

print(f"Add amount:{add_number(5,10)}")

# Sub function
def sub_number(x,y):
    sub_func = x - y
    return sub_func
print(f"Sub amount:{sub_number(10,7)}")

# Multi function
def multi_number(x,y):
    multi_func = x*y
    return multi_func
print(f"Multi amount:{multi_number(2,4)}")

# Divi function
def divi_number(x,y):
    divi = x/y
    return divi
print(f"Divi amount:{divi_number(10,5)}")
